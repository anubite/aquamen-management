/**
 * export-category-seed.js
 *
 * Reads all categories and categorization rules from the current DB and writes
 * a ready-to-commit seed file to backend/seeds/categories.js.
 *
 * Usage:
 *   node backend/export-category-seed.js
 *
 * The generated seed skips insertion when the target DB already has categories.
 */

const knex = require('knex');
const fs = require('fs');
const path = require('path');
const config = require('./knexfile');

const env = process.env.NODE_ENV || 'development';
const db = knex(config[env]);

async function main() {
    const categories = await db('transaction_categories')
        .select('name', 'description', 'color')
        .orderBy('id');

    const rules = await db('transaction_category_rules as r')
        .join('transaction_categories as c', 'r.category_id', 'c.id')
        .select('c.name as category_name', 'r.field', 'r.operator', 'r.value', 'r.priority')
        .orderBy(['c.id', 'r.priority']);

    if (categories.length === 0) {
        console.log('No categories found in DB. Nothing to export.');
        await db.destroy();
        return;
    }

    const today = new Date().toISOString().slice(0, 10);

    const catsLiteral = categories.map(c =>
        `        { name: ${JSON.stringify(c.name)}, description: ${JSON.stringify(c.description ?? '')}, color: ${JSON.stringify(c.color)} }`
    ).join(',\n');

    const rulesLiteral = rules.length > 0
        ? rules.map(r =>
            `        { category_id: catMap[${JSON.stringify(r.category_name)}], field: ${JSON.stringify(r.field)}, operator: ${JSON.stringify(r.operator)}, value: ${JSON.stringify(r.value)}, priority: ${r.priority} }`
        ).join(',\n')
        : '';

    const rulesBlock = rules.length > 0
        ? `
    const cats = await knex('transaction_categories').select('id', 'name');
    const catMap = Object.fromEntries(cats.map(c => [c.name, c.id]));

    await knex('transaction_category_rules').insert([
${rulesLiteral}
    ]);
`
        : '';

    const output =
`// Auto-generated by export-category-seed.js on ${today}
// To regenerate: node backend/export-category-seed.js

/**
 * @param { import("knex").Knex } knex
 * @returns { Promise<void> }
 */
exports.seed = async function (knex) {
    // Skip if categories already exist in this DB
    const { count } = await knex('transaction_categories').count('* as count').first();
    if (parseInt(count) > 0) return;

    await knex('transaction_categories').insert([
${catsLiteral}
    ]);
${rulesBlock}};
`;

    const outPath = path.join(__dirname, 'seeds', 'categories.js');
    fs.writeFileSync(outPath, output, 'utf8');

    console.log(`✓ Exported ${categories.length} categories and ${rules.length} rules`);
    console.log(`  → ${outPath}`);

    await db.destroy();
}

main().catch(err => {
    console.error('Export failed:', err.message);
    process.exit(1);
});
